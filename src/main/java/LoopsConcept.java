

public class LoopsConcept {

	public static void main(String[] args) {
		
		// 1. for loop
		for(int i=0;i<=27;i++) { //initialization, condition, incremental/decremental
			System.out.println(i);
			
			
			
			
			
			
			//2. while loop
			// disadvantage - it will generate infinite loop if you don't give incremental; or decremental part
			
			int a=1; // initialization
			while (a<=10) { // condintion
				System.out.println(a++);
			}
	}

}
}
